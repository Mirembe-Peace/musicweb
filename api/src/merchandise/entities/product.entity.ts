import { Entity, PrimaryGeneratedColumn, Column } from 'typeorm';

@Entity('merchandise')
export class Product {
  @PrimaryGeneratedColumn('uuid')
  id: string;

  @Column()
  name: string;

  @Column({ type: 'decimal', precision: 10, scale: 2 })
  price: number;

  @Column({ type: 'text', nullable: true })
  description: string;

  @Column()
  imageUrl: string;
}
